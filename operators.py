# Arithmetic operators
a = 10
b = 3

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiply:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)
print("Power:", a ** b)

# Comparison operators
print(a > b)
print(a == b)
print(a <= b)

# Logical operators
x = True
y = False
print(x and y)
print(x or y)
print(not x)
